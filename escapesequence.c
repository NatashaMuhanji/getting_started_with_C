#include <stdio.h>
int main(){
    printf("This is the first line \n this is the second line \n and the third line\n");
    //the \n escape sequence starts a new line in a program output

    printf("This is the first tab \t this is the second tab\n");
    //creating a horizontal tab \t 

    printf("i am natasha\\natty\n");
    //Inserts a backslash character by adding '\\'

    printf("My name is Natasha or you can call me \"mama yaga\"");
    //Inserts a double quote character

    return 0;
}